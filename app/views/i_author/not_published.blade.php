	<h1>Types of Papers MISQ Does Not Publish</h1>
	&nbsp
	&nbsp
	&nbsp

	<p><em><span class="caps">MIS</span> Quarterly</em>  has no <em>a priori</em> restrictions on the types of papers it publishes.  If a Senior Editor deems a paper to be worthy of publication, this decision is the only remaining constraint for publication.  For example, if an SE determines that a paper that develops or validates a research instrument makes a sufficient contribution, then the paper will appear in <em><span class="caps">MIS</span> Quarterly</em>. </p>

	<p>Scientometric papers will be considered, but the SEs at <em><span class="caps">MISQ</span></em> have historically required a large theoretical contribution in our Research Articles and so a simple journal ranking study, by itself, would be very unlikely to meet the standards of our Senior Editors.  If a scientometric study addresses valuable professional concerns, it could find a home in the Issues and Opinions section, however.</p>

	<p>Design science and economics of IS papers are especially welcome.  The journal has prominent SEs in both domains who are ready to handle such papers.</p>      
