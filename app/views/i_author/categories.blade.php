	<h1>Manuscript Categories</h1>
	&nbsp
	&nbsp
	&nbsp

	<p><strong>1.  Research Article.</strong> <br />
		This category provides an opportunity for authors to:
		<ul>
			<li>Make a contribution that is sufficiently original and significant so as to warrant a full-length article wherein the authors develop and present their arguments.</li>
			<li>Ground their work in theory, whether it is a new theory being advanced and tested or an existing theory.  Besides testing, authors may also be refining or challenging theories in a Research Article.</li>
			<li>Present evidence of the verity of their claims.</li>
		</ul></li>
	</p>

	<p>Authors who are developing new theory but do not test that theory in the current submission should categorize their article as <strong>5. Theory and Review Article</strong>.  See below for a description of this category.
	</p>

	<p>Most submissions to and most papers published in the <em><span class="caps">MIS</span> Quarterly</em> are Research Articles.</p>

	<p><strong>2. Research Note.</strong> <br />
		This category provides a forum for many types of concise research contribution.  This category is typically half the length of a Research Article.  Two examples of a wide variety of possibilities are:
		<ul>
			<li>Important contributions of an empirical nature that relate to topics that appear frequently in the <em><span class="caps">MIS</span> Quarterly</em> and other top journals.</li>
			<li>Discussions that relate to an important methodological issue (or issues) associated with a published article.  The connections between a Note&#8217;s content and earlier published article(s) must be clear.  Notes can arouse controversy and encourage dialogue on an important methodological issue.</li>
		</ul></li>
	</p>

	<p>A Research Note can also be thought of as a briefer form of the Research Article in the sense that the contribution may be at an acceptable level only because the research is presented in this more succinct format.
	</p>

	<p><strong>3.  Research Essay.</strong> <br />
		This category is one where authors address methodological issues.  Logical presentation of ideas in essays is critical in convincing the readership about new perspectives on how researchers should conduct research.  Research Essays may provide empirical evidence, such as simulations, or not, depending on the nature of the topic and the level of evidence required for that type of analysis.
	</p>

	<p><strong>4.  Research Commentary</strong><br />
		For this category, senior scholars are invited by the <span class="caps">EIC</span> to discuss a research stream or methodological approach and offer important insights into where the field should go.  A highlighting of seminal or key works that show how the topic has evolved is appropriate.  Commentaries conclude with a set of research questions that are worth exploring in order to answer unanswered questions.  Research commentaries are refereed by selected <em><span class="caps">MISQ</span></em> editors.
	</p>

	<p><strong>5.  Theory and Review Article</strong><br />
		Submissions to this category promote research by surveying and synthesizing prior theoretical and empirical research or by making new theoretical contributions in particular topic areas.  Review articles act as repositories for the accumulated knowledge on important topics within the information systems field, set directions for future research, and advance theory. New theoretical contributions that are well connected to prior theory and research are also welcomed.  For further details see:
		<ul>
		<li><a href="#">Objectives of the <span class="caps">MISQ</span> Theory and Review Department</a></li>
		</ul>
	</p>

	<p><strong>6.  Issues and Opinions</strong>
	</p>

	<p>This category provides a forum for the communication of well-developed and well-articulated position statements concerning emerging, paradoxical, or controversial research issues.  An Issues and Opinions article may be described as rigorously argued and/or relying on scholarly evidence.  Issues and Opinions submissions should open new areas of discourse, close stale areas, and/or offer fresh views on research topics of importance to the discipline.  They should: 
		<ul>
			<li>identify the issue(s) in terms that are easy to understand</li>
			<li>provide appropriate conceptual frameworks for the issue</li>
			<li>offer opinions and supportive arguments</li>
			<li>describe the implications of these opinions to research, practice, and/or teaching</li>
			<li>be supported, where appropriate, by empirical evidence</li>
		</ul>
	</p>

	<p>All Issues and Opinions papers will be reviewed by the Editor-in-Chief, who will send the paper out to designated referees as s/he deems appropriate
	</p>

	<h3><strong style="color:maroon;">Category Lengths</strong></h3>

	<p>Papers must be written concisely.  The following are guidelines on maximum length (excluding tables, figures, appendices, and references):
	</p>

	<p>1.  Research Articles:  40 pages <br />
		2.  Research Notes:  20 pages <br />
		3.  Research Essays:  35 pages <br />
		4.  Research Commentaries: 30 pages<br />
		5.  <span class="caps">MISQ</span> Theory and Review:  60 pages <br />
		6.  Issues and Opinions:  25 pages 
	</p>

	<p>Submissions that have an excessive number of pages may have to be returned to authors for shortening before they are sent out for review. 
	</p>          
